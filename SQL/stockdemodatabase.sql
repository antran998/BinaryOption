  CREATE OR REPLACE EDITIONABLE PROCEDURE "CLOSE_ORDER" (userID int) IS
temp1 float; temp2 float; temp3 float;
cursor c1 is SELECT PROFIT FROM TRANSACTION_ORDER WHERE ACCOUNT_ID = userID AND STATUS = 'Processing';
cursor c2 is SELECT CURRENT_MONEY FROM ACCOUNT_INFORMATION WHERE ACCOUNT_ID = userID;
cursor c3 is SELECT AMOUNT FROM TRANSACTION_ORDER WHERE ACCOUNT_ID =userID AND STATUS = 'Processing';
BEGIN
      open c1; open c2; open c3;
      fetch c2 into temp2;      
      fetch c1 into temp1;
      fetch c3 into temp3;
      temp2:=temp2+temp1+temp3;      
      close c1; close c2; close c3;
      
      UPDATE ACCOUNT_INFORMATION SET CURRENT_MONEY=temp2 WHERE ACCOUNT_ID = userID;
      COMMIT;
      UPDATE TRANSACTION_ORDER SET STATUS = 'Close' WHERE ACCOUNT_ID = userID AND STATUS = 'Processing';
      COMMIT;
END CLOSE_ORDER;

